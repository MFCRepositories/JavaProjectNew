package _009_switchCase;

public class SwitchCase {

	public static void main(String[] args) {
		int a = 4;
		switch (a) {
		case 1:
		case 2:
			System.out.println("2. yi sectiniz");
			break;
		case 3:
			System.out.println("3. yi sectiniz");
			break;
		case 4:
			System.out.println("4. yi sectiniz");
			break;
		case 5:
			System.out.println("5. yi sectiniz");
			break;
		case 6:
			System.out.println("6. yi sectiniz");
			break;
		case 7:
			System.out.println("7. yi sectiniz");
			break;
		default:
			System.out.println("Default yi sectiniz");

		}
	}

}
